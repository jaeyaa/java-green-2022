package ex05;

public class OperEx02 {
    public static void main(String[] args) {
        System.out.println(!(1 == 1)); // ==은 같다 , 괄호는 우선순위를 지정할 수 있다.
        System.out.println(!true); // !는 부정
        System.out.println(1 != 1); // 같지 않나요?
        System.out.println(1 < 2); // 2가 더 크나요?
        System.out.println(2 > 1);
    }

}
