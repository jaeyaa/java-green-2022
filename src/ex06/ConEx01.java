package ex06;

public class ConEx01 {
    public static void main(String[] args) {
        int point = 89; // 성적

        if (point >= 90) {
            System.out.println("A학점입니다");

        } else { // if가 아니면!!
            System.out.println("A학점이 아닙니다");

        }
    } // if는 만약에 , else는 그게 아니면
      // 자바에서 괄호()가 나오고 중괄호{}가 나오면 행위의 영역

}
